fn entity_cmp(p_key voidptr, p_entity voidptr) int {
	key := &EntityKey(p_key)
	ent := &Entity(p_entity)

	return C.strncmp(key.name, ent.name, key.name_size)
}

fn entity_lookup(name charptr, name_size u32) &Entity {
	key := EntityKey{name, name_size}
	table := entity_table()
	search := C.bsearch(&key, 
					&table, 
					table.len, 
					sizeof(Entity), 
					entity_cmp)

	return &Entity(search)
}